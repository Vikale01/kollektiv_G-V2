/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  /* Config code for STM32_WPAN (HSE Tuning must be done before system clock configuration) */
  MX_APPE_Config();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* IPCC initialisation */
  MX_IPCC_Init();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_TIM16_Init();
  MX_RTC_Init();
  MX_LPUART1_UART_Init();
  MX_QUADSPI_Init();
  MX_USB_Device_Init();
  MX_ADC1_Init();
  MX_RF_Init();
  /* USER CODE BEGIN 2 */
  
  BMI270_Configure();
  
  GPS_Start();
  
  BME280_Config(OSRS_2, OSRS_16, OSRS_1, MODE_NORMAL, T_SB_0p5, IIR_16);
  
  // Flash configuration
  Enable_4BYTEMODE();

  //Display initiation
  Oled_init();
  Oled_introScreen();

  findStartPos();

  /* USER CODE END 2 */

  /* Init code for STM32_WPAN */
  MX_APPE_Init();

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    switch(btnState)
    {
      case STATE_1:

        CDC_Transmit_FS(data, strlen(data));

        HAL_GPIO_TogglePin(GPIOB, LED_R_Pin);  
        HAL_GPIO_WritePin(GPIOB, LED_B_Pin, 0);
        HAL_GPIO_WritePin(GPIOB, LED_G_Pin, 0); 
        
        break;
      case STATE_2:

        if (tastScheduler >= 1){

          gpsLogo();
          if(GPS_connected)  // Check if gps has good connection before storing data.
          {
            packageDataToMem();
            sendPackageToMem();
          }

          HAL_GPIO_WritePin(GPIOB, LED_B_Pin, 0);
          HAL_GPIO_WritePin(GPIOB, LED_R_Pin, 0);  
          HAL_GPIO_WritePin(GPIOB, LED_G_Pin, 1);
          tastScheduler = 0;
        }
        break;
      case STATE_3:
        if(Custom_STN_NotificationEnabled()) // Check if someone is connected and has notification enabled
        {
          sendDataStepByStep();
        }

        HAL_GPIO_WritePin(GPIOB, LED_B_Pin, 1);
        HAL_GPIO_WritePin(GPIOB, LED_R_Pin, 0);  
        HAL_GPIO_WritePin(GPIOB, LED_G_Pin, 0);
        break;
    }
    /* USER CODE END WHILE */
    MX_APPE_Process();

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

